fn main() {
    // addition
    let sum = 5 + 10;
    println!("Sum = {}", sum);

    // substraction
    let difference = 95.5 + 4.3;
    println!("Difference = {}", difference);

    // multiplication
    let product = 4 * 30;
    println!("Product = {}", product);

    // division
    let quotient = 56.7 / 32.2;
    println!("Quotient = {}", quotient);
    let floored = 2 / 3;
    println!("Floored = {}", floored);

    // remainder
    let remainder = 43 % 5;
    println!("Remainder = {}", remainder);
}
